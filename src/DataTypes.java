
public class DataTypes  {
    public static void main(String[] args) {
        //Integer data types
        byte num1 = 100;
        short num2 = 1000;
        int num3 = 4893749;
        long num4 = 2836429386592L;

        //Floating data type
        float number5 = 3847.35234f;
        double number6 = 7439874.5353d;

        //Boolean data type
        boolean redLightSwitch = false;
        boolean blackLightSwitch = true;

        //Character data type
        char char1 = '%';
        char char2 = 'y';
        char char3 = '0';
        char char4 = '\n';

        //Strings
        String text1 = "This is a very good text";

        System.out.println(num1);
        System.out.println(number6);
        System.out.println(redLightSwitch);
        System.out.println(blackLightSwitch);
        System.out.println(char4);
        System.out.println(char2);
        System.out.println(text1);

    }
}